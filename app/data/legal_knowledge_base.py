def find_law_for_risk(category: str, description: str) -> tuple[Optional[str], Optional[str]]:
    """
    Находит подходящую норму права для риска по категории и описанию (RK).

    Args:
        category: Категория риска
        description: Описание риска

    Returns:
        tuple: (ссылка на закон, описание нормы) или (None, None) если не найдено
    """
    search_text = f"{category} {description}".lower()

    for keyword, law_info in LEGAL_KNOWLEDGE.items():
        if keyword in search_text:
            return law_info["law_reference"], law_info["law_description"]

    return None, None


